    private Connection connect() {
        String URL = "jdbc:mysql://localhost:3306/oop_project"; // Database URL
        String USER = "root"; // Database username
        String PASSWORD = ""; // Database password

        try {
            return DriverManager.getConnection(URL, USER, PASSWORD);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database connection failed: " + e.getMessage());
            return null;
        }
    }
    
    public void signup(String firstname, String lastname, String email, String contact, String city, String country, String username, String password) {
    Connection connection = connect();
    String insertQuery = "INSERT INTO users (firstname, lastname, email, contact, city, country, username, password) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";

    try (PreparedStatement pst = connection.prepareStatement(insertQuery)) {
        pst.setString(1, firstname);
        pst.setString(2, lastname);
        pst.setString(3, email);
        pst.setString(4, contact);
        pst.setString(5, city);
        pst.setString(6, country);
        pst.setString(7, username);
        pst.setString(8, password);

        int rowsInserted = pst.executeUpdate();
        if (rowsInserted > 0) {
            JOptionPane.showMessageDialog(null, "Signup successful!");
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Signup failed! Error: " + e.getMessage());
    }
}
    
    
