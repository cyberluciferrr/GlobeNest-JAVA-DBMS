    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        
        
      // Retrieve values from the input fields
    String firstname = fname.getText();
    String lastname = lname.getText();
    String email = gmail.getText();
    String contact = contactno.getText();
    String city = jTextField5.getText();     
    String country = countries.getText();
    String username = jTextField7.getText();
    String password = new String(jPasswordField1.getPassword());
    String confirmPassword = new String(jPasswordField2.getPassword());

    // Flags for validation
    boolean isValid = true;

    // Check if any fields are empty
    if (firstname.isEmpty() || lastname.isEmpty() || email.isEmpty() || contact.isEmpty() || city.isEmpty() || country.isEmpty() || username.isEmpty() || password.isEmpty() || confirmPassword.isEmpty()) {
        jLabel15.setText("Error: All fields are required.");
        jLabel15.setVisible(true);
        isValid = false;
    } 

    // Check if terms checkbox is checked
    if (!jCheckBox1.isSelected()) {
        jLabel16.setText("Error: You must accept the terms and conditions.");
        jLabel16.setVisible(true);
        isValid = false;
    } 

    // Check if username is unique
    
    boolean isUsernameUnique = true;
    
    if (!isUsernameUnique) {
        jLabel12.setText("Error: Username is already taken.");
        jLabel12.setVisible(true);
        isValid = false;
    } 

    // Check if passwords match
    if (!password.equals(confirmPassword)) {
        jLabel13.setText("Error: Passwords do not match.");
        jLabel14.setText("Error: Passwords do not match.");
        jLabel13.setVisible(true);
        jLabel14.setVisible(true);
        isValid = false;
    } 

        
    if (isValid) {
        try {
            
            SignUpFrame p1 = new SignUpFrame(); // Ensure OOPProject is correctly defined
            p1.signup(firstname, lastname, email, contact, city, country, username, password);
            
        } catch (Exception e) {
            System.err.println("Error during sign-up: " + e.getMessage());
            jLabel15.setText("Error: Unable to complete the sign-up process.");
            jLabel15.setVisible(true);
        }
        LoginFrame lf=new LoginFrame();
            lf.show();
            dispose();
    }

    }//GEN-LAST:event_jButton2MouseClicked

